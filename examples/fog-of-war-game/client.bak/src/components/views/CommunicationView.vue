  <div class="communication-view">
    <div class="view-header">
      <h2>ðŸ’¬ Communications</h2>
      <div class="channel-tabs">
        <button
          v-for="channel in channels"
          :key="channel.id"
          class="channel-tab"
          :class="{ active: activeChannel === channel.id }"
          @click="activeChannel = channel.id"
        >
          {{ channel.icon }} {{ channel.label }}
        </button>
      </div>
    </div>

    <div class="messages-container">
      <div
        v-for="message in filteredMessages"
        :key="message.id"
        class="message-card"
        :class="`message-${message.type}`"
      >
        <div class="message-header">
          <span class="message-sender">{{ getSenderName(message.senderId) }}</span>
          <span v-if="message.receiverId" class="message-receiver">
            â†’ {{ getSenderName(message.receiverId) }}
          </span>
          <span class="message-time">{{ formatTime(message.timestamp) }}</span>
        </div>
        <div class="message-content">{{ message.content }}</div>
      </div>

      <div v-if="filteredMessages.length === 0" class="empty-state">
        <div class="empty-icon">ðŸ’¬</div>
        <div>No messages in this channel yet</div>
      </div>
    </div>

    <!-- Message composition (future feature) -->
