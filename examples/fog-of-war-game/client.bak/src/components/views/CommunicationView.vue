<script setup lang="ts">
import { ref, computed } from 'vue'
import { useGameStore } from '@/stores/gameStore'

const gameStore = useGameStore()
const activeChannel = ref<'all' | 'red' | 'blue' | 'enemy'>('all')

const channels = [
  { id: 'all', label: 'All Messages', icon: 'ðŸ“¢' },
  { id: 'red', label: 'Red Team', icon: 'ðŸ”´' },
  { id: 'blue', label: 'Blue Team', icon: 'ðŸ”µ' },
  { id: 'enemy', label: 'Cross-Team', icon: 'âš”ï¸' }
]

const filteredMessages = computed(() => {
  const messages = gameStore.recentMessages

  if (activeChannel.value === 'all') {
    return messages
  } else if (activeChannel.value === 'red') {
    return messages.filter(m => m.type === 'team' && isRedCommander(m.senderId))
  } else if (activeChannel.value === 'blue') {
    return messages.filter(m => m.type === 'team' && isBlueCommander(m.senderId))
  } else if (activeChannel.value === 'enemy') {
    return messages.filter(m => m.type === 'enemy')
  }

  return messages
})

function getSenderName(agentId: string): string {
  if (agentId === 'system') return 'System'

  const commander = gameStore.gameState?.commanders.find(c => c.agentId === agentId)
  return commander?.name || agentId
}

function isRedCommander(agentId: string): boolean {
  const commander = gameStore.gameState?.commanders.find(c => c.agentId === agentId)
  return commander?.team === 'red'
}

function isBlueCommander(agentId: string): boolean {
  const commander = gameStore.gameState?.commanders.find(c => c.agentId === agentId)
  return commander?.team === 'blue'
}

function formatTime(timestamp: number): string {
  const date = new Date(timestamp)
  return date.toLocaleTimeString()
}
</script>

